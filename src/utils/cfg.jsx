export const SIZE = 100;
export function COLOR(color) {
    switch (color) {
        case 0:
            return "#aaa";
        case 1:
            return "#5af";
        case 2:
            return "#fb0";
        case 4:
            return "#05DF72";
        case 8:
            return "#ED6AFF";
        default:
            return "#aaa";
    }
}

